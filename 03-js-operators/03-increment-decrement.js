// Increment; used to increase the number by one
let num1 = 5;

//let num2 = ++num1; // pre-increment
let num2 = num1++; //past increment: increase num1 by 1 but not here 

console.log(num1); // 6
console.log(num2); //5








// decrement: used to decrease the number by one 

let n1 = 10;

let n2 = --n1 * 5;

console.log(n2); //45


let number = 25;

number++;

console.log(number-- * 10); // 260











